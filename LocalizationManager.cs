        private static void ApplyLanguage()
        {
            var app = Application.Current;
            if (app == null) return;

            var currentDict = _resources[_currentLanguage];
            
            // Supprimer les anciennes ressources de localisation
            var toRemove = new List<ResourceDictionary>();
            foreach (var dict in app.Resources.MergedDictionaries)
            {
                if (dict.Source == null && dict.Contains("AppName"))
                {
                    toRemove.Add(dict);
                }
            }
            foreach (var dict in toRemove)
            {
                app.Resources.MergedDictionaries.Remove(dict);
            }

            // Ajouter les nouvelles ressources
            app.Resources.MergedDictionaries.Add(currentDict);
            
            LanguageChanged?.Invoke(null, EventArgs.Empty);
        }

        public static string GetString(string key)
        {
            if (_resources.TryGetValue(_currentLanguage, out var dict) && dict.Contains(key))
            {
                return dict[key] as string ?? key;
            }
            return key;
        }

        public static void SetLanguage(Language lang)
        {
            CurrentLanguage = lang;
        }
